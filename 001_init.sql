    user_id UUID NOT NULL,
    transaction_type VARCHAR(10) NOT NULL CHECK (transaction_type IN ('bet', 'win')),
    amount INTEGER NOT NULL CHECK (amount > 0),
    timestamp TIMESTAMP NOT NULL
);

